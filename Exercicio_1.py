Lista = [5, 7, 2, 9, 4, 1, 3]
print("Lista = ", Lista)
print("O tamanho da lista é ", len(Lista)) #A
print("O maior elemento da lista é ", max(Lista)) #B
print("O menor elemento da lista é ", min(Lista)) #C
print("A soma dos elementos da lista é ", sum(Lista)) #D
Lista.sort()
print("Lista em ordem crescente: ", Lista) #E
Lista.reverse()
print("Lista em ordem decrescente: ", Lista) #F